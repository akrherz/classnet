===subject:Hyaline Membrane Disease
===message:http://www.pbcc.cc.fl.us/eissweb/vshaver/hmd.htm

Hyaline Membrane Disease (aka Respiratory Distress Syndrome) is a disease that affects newborn babies, especially those born prematurely.  It is caused by a lack of pulmonary surfuctant in the alveoli, a substance that reduces surface tension in the alveoli.  Without this substance the alveoli collapse at the end of every respiration, resulting in respiratory failure in the newborn.
