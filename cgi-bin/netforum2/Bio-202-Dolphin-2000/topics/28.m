===message:Tuberculosis (TB) is caused by a bacterial infection.  It can infect all parts of the body, but 80% of the time it infects the lungs.  This infection is called Pulmonary TB.  It progresses through three stages.  The first stage, called "early infection," is where bacteria lodge in the lungs and the body forms a tubercle as an immune response.  This stage often heals without being noticed or might be passed off as a "cold or flu".  The second stage called "Dormant TB," is the dormant stage where germs may be widespread throughout the body, but don't cause any symptoms.  The third stage causes sores in the lungs and other parts of the body.  It is referred to as "Active TB".  Antibiotics can be used to treat TB, but there is a resistant strain of TB that is developing due to improper use of antibiotics.

