===subject:Hep. C research
===message:The website is: http://partners.health.gov.au/hfs/pubhlth/strateg/hiv_hepc/hepc/register/109.htm

This was about an Australian research project to investigate the stability of Hepatitis C in the environment using a surrogate virus, the bovine viral diarrhoeal virus.  This project was just recently finished, but its report isn't readily available yet.  However, it did have three major aims.  First, it set out to measure the stability of the Hepatitis C and BVDV genetic material in blood and serum solutions at room temperature in closed plastic tubes, which simulate used syringes, over four and nine weeks respectively.  Secondly, it was to measure the infectivity of the BVDV from the above samples being studied.  Finally, it estimated the amount of time Hepatitis C may be infectious in the environment, with specific reference to injecting drug users.  This was all to provide a better understanding of the mechanisms of the spread of Hepatitis C in the injecting drug user population.


