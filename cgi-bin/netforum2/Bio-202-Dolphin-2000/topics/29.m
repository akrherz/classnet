===message:At the Austin Research Institute website, http://www.ari.unimelb.edu.au/res0999.htm, I read about the Cellular Immunity Labratory's efforts to study the role of natural killer (NK) cells in cancer and viral infections.  They have determined that NK cells play an important part in the defense against early herpes virus infections, but their research suggests that herpes posses some mechanism of evading the subsequent immune response.  They have studied this mechanism by observing the mouse-equivalent to herpes, mouse cytomegalovirus (MCMV), as it avoids NK cell immune attack on the virus.  Researchers have isolated a protein, m144, that seems to protect infected cells from NK cell killing.  Their experiments suggested that m144 may prevent the normal recruitment and activation of NK cells at the site of infection.
