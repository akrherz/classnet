===subject:Recurrent Pregnancy Loss
===message:For many years recurrent pregnagncy loss was often labeled as "unexplained," but recent research shows that as many as 80% of these losses may be attributed to antibody response problems.  In autoimmune problems the immunologic response of the mother to her own proteins causes the rejection of the pregnancy.  This problem can be treated with low-dose heparin, aspirin, and prednisone prior to conception.  Alloimmune problems are those in which the mother's immunologic response to the man's genetic contribution/protein causes the rejection of the pregnancy.  This problem can be treated by immunizing the woman with her husband's white blood cells.  Both of these problems can be determined through blood tests and treatment results in a 10-80% increase in the chance of a live birth.
