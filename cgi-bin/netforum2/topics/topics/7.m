===message:Huntington's Disease<P>
Eric Underbakke<P>

	Huntington's disease is a rare and fatal genetic disease first diagnosed by George Huntington in 1872 in his paper called "On Chorea"(Jankovic, 1997).  Nearly 25,000 people are currently suffering the effects of Huntington's disease, and 150,000 have the mutant gene(Henahan, 1995)<P>
	The gene causing Huntington's disease is located on the 4th chromosome on the end of the short arm.  The gene itself is called ‘huntingtin'(OMIM, 1998).  The affliction is an autosomal dominant trait, so every person carrying the mutant gene will eventually show symptoms of the disease, if they live long enough.  The actual mutation is an increase in the number of CAG repeats in the huntingtin gene.  Normal people have between 9 to 37 repeats, while people showing symptoms of Huntington's disease usually have between 37 and 86 CAG repeats(Shapira, 1997).  The exact mechanism that causes the disease is unclear, however, the disease is known to be a progressive loss of mental functioning resulting from brain cell deaths in the caudate nucleus, the most medial of the four basal ganglia in the brain.  The brain cell death is accompanied by a loss of some neurotransmitters and an increase in the levels of dopamine in the brain(OMIM, 1998).<P>
	The symptoms of Huntington's disease usually appear between the ages of 30 and 40.  The affliction is characterized by choleric movements (rapid, involuntary, movement flowing from one body part to another), dementia (personality changes, apathy, paranoia, hostility, hallucinations), rigidity, and sometimes seizures(Jankovic, 1997).  Victims of Huntington's disease are usually institutionalized and die within 10 to 20 years from aspiration, infection, and poor nutrition.<P>
	Mutations of the huntingtin gene can occur when the gene is passed to a sperm cell.  The number of CAG repeats generally increases as the gene is passed on to germ cells(OMIM, 1998).  Scientists have found that more CAG repeats in the gene, the earlier the symptoms appear, and earlier the victim dies.  Interestingly, when the gene is passed to an egg (ie the disease comes from the mother), the mutation is far less extensive.  The CAG repeats slightly increase in number, but the effects on an afflicted offspring are negligible.  The offspring will still have Huntington's disease, but the disease will not appear any earlier than it did in the mother(Shapira, 1997).<P>

