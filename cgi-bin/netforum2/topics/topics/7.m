===subject:Bloom Syndrome
===message:	A defect in the human gene BLM causes Bloom Syndrome, a congenital disorder characterized by many symptoms, including dwarfism, telangiectasia (chronic dilation of capillaries causing raised dark-red blotches on the skin), and spotty hypo- and hyperpigmentism, to name a few of the more noticeable clinical symptoms.  In addition, sufferers of this condition have highly compromised immune systems, making them highly susceptible to life-threatening infections.  Bloom Syndrome patients also have a learning disability and are prone to cancer.  (OMIM, 1998)  <P>
	The gene BLM is located on chromosome 15, and has an autosomal recessive inheritance pattern.  Bloom Syndrome has three known allelic variations.  The first is a 6 base pair deletion/7 base pair insertion at nucleotide 2281, causing a stop codon a few codons after amino acid 736.  The second allele is a deletion of CAA at nucleotide position 631-633, causing a stop codon at amino acid position 186.  The final variation is a T to C transition at nucleotide 2596 which results in an isoleucine to threonine substitution at amino acid position 843.  (OMIM, 1998) <P>

