===message:Achondroplasia is a genetic disorder of bone growth that is evident at birth. It affects about one in every 20,000 births and it occurs in all races and in both sexes. Its depiction in ancient Egyptian art makes it one of the oldest recorded birth defects. <P>
	The word Achondroplasia is derived from Greek and means "without cartilage formation," although individuals with Achondroplasia do have cartilage. During fetal development and childhood, cartilage normally develops into bone, except in a few places, such as the nose and the ears. In individuals with Achondroplasia, something goes wrong during this process, especially in the long bones (such as those of the upper arms and thighs). The rate at which cartilage cells in the growth plates of the long bones turn into bone is slow, leading to short bones and reduced height.<P>
	It is a common  autosomal dominant disorder.  In some cases, a child inherits Achondroplasia from a parent who also has the condition. If one parent has the condition and the other does not, with each pregnancy there is a 50 percent chance that each child will be affected. If both parents have Achondroplasia, there is a 50 percent chance that the child will inherit the condition, a 25 percent chance that the child will not have it, and a 25 percent chance that the child will inherit one abnormal gene from each parent and have severe skeletal abnormalities that lead to early death. A child who does not inherit the gene will be completely free of the condition, and cannot pass it on to his or her own children. <P>
	Achondroplasia is caused by a failure of proliferation and column formation of epiphysial cartilage cells, that is, by a defect in endochondral bone formation which impairs the longitudinal growth of the tubular bones. Point mutations in the FGR3 gene (encoding fibroblast growth receptors) on human chromosome 4 p segregate with disease in achondroplasia families (Rousseau, F., et al. Nature 371:252-254, 1994).<P>
	In most cases (over 80 percent), however, Achondroplasia is not inherited but results from a new mutation (change) that occurred in the egg or sperm cell that formed the embryo. The parents of children with Achondroplasia resulting from new mutations are usually average-sized.  Analysis for the two most common mutation reported in individuals with Achondroplasia (ACH1138G->C and ACH1138 G->A in the FGFR3 gene). (Rousseau, F., et al. Nature 371:252-254, 1994).<P>




