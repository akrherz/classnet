===message:HEREDITARY JUVENILE OPEN ANGLE GLAUCOMA (GLC1A type) <P>

Glaucoma is a major cause of blindness and is characterized by progressive degeneration of the optic nerve and is usually associated with elevated intraocular pressure. Though glaucoma is typically a disease of late adulthood, an uncommon hereditary form manifests itself in childhood or early adulthood. This form, called hereditary juvenile open angle glaucoma type GLC1A (OMIM ID#137750), has a dominant, autosomal pattern of transmission. This disease should not be confused with another form of juvenile glaucoma (OMIM ID# 231500) that is inherited recessively. It should not also be confused with primary infantile glaucoma (type GLC3A, OMIM ID# 231300 - see the assignment submitted by Ellie Nunez for the gene that causes this disease) or with adult-onset glaucoma, which occurs in the late forties or the early fifties (OMIM ID# 137760). <P>

In a variety of studies conducted over a period of 50 years (see, for example, Zorab(1932), Martin and Zorab(1974), Johnson et al. (1993), Lotufo et al. (1989), Morissette et al. (1995)), researchers found that those affected by the disease exhibited a variety of symptoms. In some cases, affected persons display abnormal pigmentation of the irises, with the irises appearing unusually dark, though it is unclear whether these are actually caused by glaucoma, or by a complementary disorder called IGDA (iridogoniodysgenesis, OMIM ID# 601631). Many of those affected are myopic, and the disease appears to be more prevalent among Americans of African ancestry. However, the most common symptom is high intraocular pressure, that needs to be to be reduced surgically to prevent loss of vision. <P>

In the past few years, researchers such as Johnson et al.(1993), Sheffield et al.(1993) and Wiggs et al. (1994) discovered from several independent samples that the gene for juvenile glaucoma maps to the location 1q21-q31 on the first human chromosome. This gene is referred to in the literature as GLC1A. More recently, Escribano et al. (1995) and Stone et al. (1997) discovered that a gene that was isolated from the ocular ciliary body (a tissue responsible for regulating aqueous humor secretion and intra-ocular pressure) mapped to the same 1q region as the glaucoma gene GLC1A. This gene encodes a protein called Myocilin and therefore is referred to in the literature as MYOC (or TIGR, after "trabecular meshwork-induced glucocorticoid response protein").  Moreover, Stone et al. (1997) found that one of three mutations in the MYOC gene was significantly greater in a sample of glaucoma patients than in a corresponding control sample (3.9% to 0.2%). Stone hypothesized that the mutations in the MYOC gene may increase intra-ocular pressure by obstructing aqueous outflow from the eye, or by causing some muscle abnormality in the ciliary body. Subsequently, Adam et al. (1997) described 5 new mutations in the MYOC gene in 8 French families with juvenile glaucoma. The evidence seems to suggest, therefore, that the MYOC and the GLC1A genes are identical, and that mutations in this  gene could be a possible cause for juvenile glaucoma. In the Genome Data Base (GDB), the records for the MYOC and the GLC1A genes have been merged.<P>

The protein coded by the MYOC gene is a 497-amino acid polypeptide. As mentioned above, eight allelic variants have been discovered. They are:<P>

1) tyr430-to-his <P>
2) gly357-to-val <P>
3) gln361-to-ter <P>
4) pro370-to-leu <P>
5) ile477-to-ser <P>
6) asn480-to-lys <P>
7) pro370-to-leu <P>
8) gly367-to-arg <P>

Note: even though 8 alleles are shown in the database, 4 and 7 appear to be identical - so only 7 seem to be present. <P>

In summary, significant progress has been made in identifying a gene whose mutations appear to be responsible for causing juvenile glaucoma. According to Stone, it is estimated that nearly 100,000 individuals in the U.S. may be suffering from this disease. This would make GLC1A-cause glaucoma the most molecularly recognizable form of blindness. <P>

