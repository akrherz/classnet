===subject:Adrenoleukodystrophy
===message:Adrenoleukodystrophy<P>

Adrenoleukodystrophy (ALD) is a serious disease which affects approximately 1 in 100,000 people with most of them male.  This disease is X-linked recessive and is believed to be the ALD gene.  It’s located on the locus of the X chromosome--Xq28  (“Adrenoleukodystrophy”).<P>

There are two forms of the ALD disease:  (1) a childhood form that leads to total disability or death the first ten years of life and (2) an adult form (also called Adrenomyeloneuropathy) that progresses slower and allows the individual to live a near-normal life.  In the childhood form, early symptoms of neurological damage include hyperactivity, difficulty with speech, impaired spatial orientation, visual problems, and seizures.  Later, symptoms persist with a complete loss of speech, inability to swallow, increased paralysis, and endocrinological problems which leads to a vegetative state. Neurological damage occurs in the adult form which includes degeneration of the spinal cord, and peripheral nervous system, progressive loss of cognitive ability and endocrinological symptoms (DeLaCruz, Kho, and Tran).<P>

There have been various mutations hypothesized for Adrenoleukodystrophy.  The main one is a substitution of an Adenine base for a Guanine base in codon 617 of the ALD gene.  A mis-sense point mutation (mutation resulting in the coding of the wrong amino acid) on exon 6 and a non-sense mutation (a mutation that encodes a stop codon) on exon 4.  Another is a 38 base pair deletion causing extensive mutations and a 3 base pair deletion that results in the loss of the use of ATP binding domain of an ALD protein (DeLaCruz, Kho, and Tran).<P>

Unfortunately there is no cure for this disease.  There are a few treatments, but are not very effective and cannot reverse damage already done to the individual.  Bone marrow transplants have been tried, but have had poor results  (DeLaCruz, Kho, and Tran).<P>

