<P>Neurofibrimin is a genetic disorder that is associated with
numerous diseases and defects in the central and peripheral nervous
system (Xu, et al. 1990). Also called NF1, neurofibromin is "is a
common... disorder characterized by abnormalities in multiple tissues
derived from the embryonic neural crest" (Marchuk, et al. 1991). NF1
is located on chromosome 17q11.2, and is autosomal dominant
(Bernards, et al. 1992). Neurofibromin is one of two types of
neurofibromin, the other being neurofibromin 2. Although NF1 has been
called peripheral neurofibromatosis, the disease has also been
associated with disorders of the central nervous system as well. Some
effects include "tumors of the central nervous system, which include
astrocytomas of the visual pathways, ependymomas, meningiomas, and
some primitive neuroectodermal tumors. The most common neuroimaging
abnormality...has been a high signal intensity lesion in the basal
ganglia, thalamus, brain stem, cerebellum, or subcortical white
matter referred to as an 'unidentified bright object' (UBO)...thought
to represent sites of vacuolar change" (Parazzini, et al. 1995).
Neurofibromin has been affiliated with other disorders as well,
including scoliosis, mental retardation and hypertension (Clinical
synopsis, 1998).</P>

